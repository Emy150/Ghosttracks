    private void lblEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblEditarMouseClicked
        System.out.println("¡Clic detectado en Editar!");
        System.out.println("-> ¿Controlador es null?: " + (this.controlador == null));
        System.out.println("-> ¿ProductoActual es null?: " + (this.productoActual == null));

        if (this.controlador != null && this.productoActual != null) {
            controlador.solicitarEdicion(this.productoActual);
        }
    }//GEN-LAST:event_lblEditarMouseClicked

    private void lblEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblEliminarMouseClicked
        System.out.println("¡Clic detectado en Eliminar! Controlador: " + controlador);
    
        if (this.controlador != null && this.productoActual != null) {
            itson.org.ghosttracks.presentacion.administrador.PantallaGestionArticulos pantallaPrincipal = 
                (itson.org.ghosttracks.presentacion.administrador.PantallaGestionArticulos) 
                javax.swing.SwingUtilities.getAncestorOfClass(
                    itson.org.ghosttracks.presentacion.administrador.PantallaGestionArticulos.class, 
                    this
                );

            if (pantallaPrincipal != null) {
                controlador.solicitarEliminacion(pantallaPrincipal, this.productoActual);
            } else {
                System.out.println("Advertencia: No se encontró PantallaGestionArticulos en la jerarquía.");
            }
        }
    }//GEN-LAST:event_lblEliminarMouseClicked


