    public void cargarDatos(ProductoDTO producto, ControladorGestionArticulos controlador) {
        this.productoActual = producto;
        this.controlador = controlador;

        lblTitulo.setText(producto.getTitulo());
        lblArtista.setText(producto.getArtista());
        lblTipo.setText(producto.getTipo()); 
        lblPrecio.setText(String.format("$%.2f", producto.getPrecio()));
        lblStock.setText(String.valueOf(producto.getStockInicial()));

        if(producto.getStockInicial() > 0) {
            lblEstado.setText("Disponible");
            lblEstado.setForeground(new Color(0, 153, 0));
        } else {
            lblEstado.setText("Agotado");
            lblEstado.setForeground(new Color(204, 0, 0));
        }
        
        lblEliminar.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));
        lblEditar.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));
    }
