    public void cargarDatosFormulario(ProductoDTO producto, ControladorGestionArticulos controlador) {
        this.productoActual = producto;
        this.controlador = controlador;

        // Cambiar dinámicamente el título superior
        label.setText("EDITAR ARTÍCULO");
        this.skuOriginal = producto.getSku();

        // Rellenar los campos de texto
        txtTitulo.setText(producto.getTitulo());
        txtArtista.setText(producto.getArtista());
        txtPrecio.setText(String.valueOf(producto.getPrecio()));
        txtStock.setText(String.valueOf(producto.getStockInicial()));

        // Seleccionar el Tipo de Artículo
        cmbTipoArticulo.setSelectedItem(producto.getTipo());

        cmbGenero.removeAllItems();
        cmbGenero.addItem("Seleccionar género");

        // Traemos los géneros reales desde el controlador (los DTOs con sus IDs de Mongo)
        java.util.List<GeneroDTO> listaGeneros = controlador.obtenerListaGeneros();
        GeneroDTO generoASeleccionar = null;

        for (GeneroDTO g : listaGeneros) {
            cmbGenero.addItem(g);
            // Si el ID o el nombre coincide con el del producto que estamos editando, lo guardamos para seleccionarlo
            if (g.getId().equals(producto.getGenero()) || g.getNombre().equalsIgnoreCase(producto.getGenero())) {
                generoASeleccionar = g;
            }
        }

        if (generoASeleccionar != null) {
            cmbGenero.setSelectedItem(generoASeleccionar);
        }
    }
