    private void botonRedondeado1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRedondeado1ActionPerformed
        try {
            // Validación de seguridad elemental
            if (this.productoActual == null) {
                JOptionPane.showMessageDialog(this, "Error crítico: No se encontró el producto original a editar.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Recuperar el ID original de MongoDB del producto
            String idProducto = this.productoActual.getIdProducto(); 
            String titulo = txtTitulo.getText().trim();
            String artista = txtArtista.getText().trim();
            String tipo = cmbTipoArticulo.getSelectedItem().toString();

            // Validar selección del género
            Object itemSeleccionado = cmbGenero.getSelectedItem();
            if (itemSeleccionado == null || itemSeleccionado instanceof String) {
                JOptionPane.showMessageDialog(this, "Por favor, selecciona un género válido de la lista.", "Advertencia", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // Obtener el ID del género de forma segura
            GeneroDTO generoSelected = (GeneroDTO) itemSeleccionado;
            String idGenero = generoSelected.getId();

            Double precio = Double.parseDouble(txtPrecio.getText());
            Integer stock = Integer.parseInt(txtStock.getText());

            // Usamos null si no se seleccionó un nuevo archivo de imagen (para mantener la actual en el BO)
            java.io.File archivoImagen = null; 

            // Enviar parámetros al controlador para su procesamiento
            controlador.procesarEdicionProducto(idProducto, titulo, artista, tipo, idGenero, precio, stock, archivoImagen);

        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this, "Por favor, ingresa valores numéricos válidos en Precio y Stock.", "Error numérico", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurrió un error inesperado: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_botonRedondeado1ActionPerformed

